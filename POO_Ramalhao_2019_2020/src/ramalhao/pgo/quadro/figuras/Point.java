package ramalhao.pgo.quadro.figuras;


public class Point {
private int x;
private int y;

public Point (int posicaoX,int posicaoY)

{
		
 super();
 this.x = posicaoX;
 this.y = posicaoY;
}

public int getX() {
	return x;
}

public void setX(int x) {
	this.x = x;
}

public int getY() {
	return y;
}

public void setY(int y) {
	this.y = y;
}
 
}
